// Exercise 1:
// Write a program to read two integer values, and then display the sum of these numbers with a message as in the examples.

package section04.exercises;

public class Exercise01 {
    public static void main (String[] args) {

        int a = 10;
        int b = 30;

        int sum = a + b;
        System.out.printf("The sum of %d and %d is %d%n", a, b, sum);
    }
}
